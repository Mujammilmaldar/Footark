                                <p class="instruction-text">Ankle arthroscopy (ahr-THROS-ko-pe) is a procedure to check or repair a damaged or diseased ankle joint. The ankle joint is where your lower leg bones and the bone on the top of your foot meet. Ankle arthroscopy may be done to check an injured, unstable, stiff, or painful ankle. If you have these problems after treatment with exercises, special shoes, a splint or other measures, you may need an ankle arthroscopy. This procedure shows the condition of your bones, cartilage, tendons, ligaments, and other tissues. Tendons are strong elastic tissues that connect muscles to bones. Ligaments connect one bone to another.</p>
                                 
