                            <h3>Manual</h3>
                            <p>Soft tissue mobilization to ankle/foot/effleurage for edema.</p>
                            <p>Avoid direct palpation to suture line (keep 2 inch "no touch zone" around portals x 4 weeks).</p>

                            <h3>Exercises</h3>
                            <p>Toe curls, toe spreads, gentle foot movement in boot, straight leg raises, knee flexion/extension.</p>
                            <p>Well-leg cycling, weight training, and swimming for cardiovascular.</p>

