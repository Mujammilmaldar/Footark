                    <h3>General Considerations</h3>
                    <p>Time frames mentioned in this protocol should be considered approximate with actual progression based upon clinical presentation, physician appointments as well as continued assessment by the treating practitioner should dictate progress.</p>
                    <p>Avoid forceful active and passive range of motion of the Achilles for 10 - 12 weeks.</p>
                    <p>Carefully monitor the tendon and incisions for mobility and signs of scar tissue formation. Regular soft tissue treatments (i.e. scar mobilization and friction massage) to decrease fibrosis.</p>
                    <p>No direct scar mobilization at surgical portals until 4 weeks post operative, "no touch zone" 2 inches from portals. See wound care protocol for full details.</p>
                    <p>All exercises should be carefully observed for any signs of compensation or guarding.</p>
                    <p>No running, jumping, or ballistic activities for 6 months. Aerobic and general conditioning throughout the rehabilitation process.</p>
                    <p>M.D./nurse appointments at Day 1-2, Day 14, 1 month, 3 months, 6 months, and 1 year post-op.</p>
                </div>

