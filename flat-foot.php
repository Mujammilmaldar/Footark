                        <h2>Understanding Flat Foot</h2>
                        <div class="content-with-image">
                            <div class="content-text">
                                <p>Most people have an arch along the inside of the foot. This arch creates a space between the ground and the foot. The arch develops in childhood, by the age of 9 - 10 years. In some people the arch is minimal or absent, these individuals are said to have "flat feet." In the majority of cases this "flatness" is NORMAL, and nothing to worry about.</p>
                                
                                <p>The shape of the bones sets the shape of the foot. The ligaments and muscles form secondary supports. There is a large muscle on the inner side of foot, the Tibialis Posterior muscle, which supports the arch. Damage to the Tibialis Posterior tendon can cause the foot to flatten.</p>
