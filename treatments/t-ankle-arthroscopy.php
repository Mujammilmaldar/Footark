                        <p><b>Nonsurgical Rehabilitation</b></p>
                        <p>Even if you don't need surgery, you may need to follow a program of rehabilitation exercises. Your doctor may recommend that you work with a physical therapist. Your therapist can create a program to help you regain ankle function. It is very important to improve strength and coordination in the ankle.</p>
                        <p><b>After Surgery</b></p>
                        <p>After debridement surgery, patients are usually placed in an ankle splint. Patients begin by using crutches. The amount of weight put on the foot is gradually increased over a period of one to two weeks. Patients generally advance quickly in rehabilitation and are able to resume normal activity within four to six weeks.</p>
