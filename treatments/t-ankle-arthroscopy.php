                    <div class="content-section" id="debridement">
                        <h3>Debridement</h3>
                        <p><b>Debridement :</b>Debridement is the most common surgery for anterior ankle impingement. Many surgeons prefer to perform this procedure with an arthroscope. An arthroscope is a tiny TV camera that can be inserted into a very small incision. It allows the surgeon to see the area where he or she is working on a TV screen.</p>
                        <p>To begin, two small incisions are made through the skin on each side of the impingement area. The surgeon inserts the arthroscope to see which area of the tendons or joint capsule are irritated and thickened. The arthroscope lets the doctor see if a meniscoid lesion (mentioned earlier) is present. A small shaver is used to clear away (debride) irritated tissue from the affected ligaments. The surgeon also debrides the tissue forming a meniscoid lesion and any areas of the joint capsule that are inflamed. Small forceps may also be used to clear away irritated or inflamed tissue. Small bone spurs on the tibia or talus are removed. If the spurs are large, the surgeon may decide to create a new incision over or next to the spur. This allows a special instrument, called an osteotome, to be inserted. The surgeon uses the osteotome to carefully remove these larger bone spurs. Before concluding the procedure, a fluoroscope is used to check the debridement and to make sure no bony fragments remain. A fluoroscope is a special X-ray machine that allows the surgeon to see a live X-ray picture on a TV screen during surgery. When the surgeon is satisfied that debridement and removal of bone fragments is complete, the skin is stitched together.</p>
                    </div>

                    <div class="content-section" id="excision">
                        <h3>Os Trigonum Excision</h3>
                        <p>The goal of an os trigonum excision is to carefully remove (excise) the os trigonum to alleviate pinching of the tissues above or below it. It is standard to use an open surgical method which requires a one- to two-inch incision over the outer part of the back of the ankle. An arthroscope is not routinely used for os trigonum excision because there are many nerves and blood vessels in the back of the ankle that could be injured by an arthroscope.</p>
                        <p>This surgery begins by placing the patient face down on the operating table. The surgeon makes a small incision over the lateral side of the back of the ankle, just behind the outer anklebone. A retractor is used to carefully hold the nearby tendons, nerves, and blood vessels out of the way. The surgeon locates the os trigonum. A scalpel is usually sufficient to dissect the os trigonum. However, if a bony bridge binds the os trigonum to the talus, the surgeon may need to use a chisel or osteotome. A fluoroscope is used to check for any remaining bony fragments. When the surgeon is satisfied that all bone fragments have been removed, the skin is stitched together. Patients are placed in a special splint designed to protect the ankle and to keep the foot from pointing downward.</p>
                    </div>

                    <div class="content-section" id="rehabilitation">
