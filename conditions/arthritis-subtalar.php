                    <div class="content-section" id="symptoms">
                        <h3>Symptoms and Pain Location</h3>
                        <p>Patients with subtalar arthritis will complain of pain on one or both sides of the foot, just below the ankle bones (malleoli). This is commonly illustrated by the patient encircling the affected foot with their fingers, just below the level of the bony prominence on either side of the ankle (malleoli). The hollow just in front of the outside ankle bone (the sinus tarsi) is another common location of pain. The sinus tarsi is a space surrounded by the three contact areas between the talus and calcaneus, that comprise the subtalar joint.</p>
                    </div>

                    <div class="content-section" id="joint-function">
                        <h3>Subtalar Joint Function</h3>
                        <p>The subtalar joint is largely responsible for allowing the foot to accommodate uneven terrain by moving the hindfoot from side to side (inversion and eversion). Walking on uneven surfaces places a great deal of stress on the subtalar joint and may be difficult, if not impossible, to accomplish in patients with subtalar arthritis.</p>
                    </div>

                    <div class="content-section" id="causes">
                        <h3>Causes of Subtalar Arthritis</h3>
                        <p>Most subtalar arthritis is caused by a previous trauma, usually a calcaneal fracture, although certain fractures involving the talar body may also cause subtalar arthritis. Other causes include rheumatoid arthritis, or the abnormal loading of the subtalar joint associated with malalignment of the heel bone (calcaneus). The load applied to the subtalar joint can be unevenly distributed in cases where the heel bone turns in (varus alignment seen with a high arched foot) or out (valgus alignment associated with a flat foot).</p>
                    </div>

