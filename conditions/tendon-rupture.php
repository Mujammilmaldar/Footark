                    <div class="content-section" id="symptoms">
                        <h3>What are the symptoms of ruptured Achilles tendon?</h3>
                        <p>The history of Achilles rupture is usually classically described as a feeling of being kicked to the Achilles area and sometimes even a very audible "crack" or "snap" is heard. There is an immediate inability to continue with the activity and well-localized pain. In the vast majority of cases bruising behind and to the sides of the ankle and swelling occurs rapidly. You should seek medical advice immediately.</p>
                    </div>

                    <div class="content-section" id="duration">
                        <h3>How long will symptoms last if I do nothing?</h3>
                        <p>Usually two weeks after a complete rupture there is usually very little pain anymore. At this stage it's more common to notice a weakness of gait, possibly a feeling of instability and also likely a limp. It will also not be possible to raise yourself onto tip toes on the injured ankle.</p>
                    </div>

                    <div class="content-section" id="diagnosis">
                        <h3>If I think I have an Achilles rupture do I need a scan?</h3>
                        <p>If you think you may have torn your Achilles tendon, it is better to get it checked by an orthopaedic surgeon as soon as possible. Early diagnosis with the help of clinical examination and confirmed by a scan can make a difference in it being more treatable, leading to better long-term outcome.</p>
                    </div>

                    <div class="content-section" id="delayed-treatment">
                        <h3>What happens if the treatment of Achilles rupture is delayed?</h3>
                        <p>Without treatment, the tendon edges remain apart and heal incorrectly with excessive scar tissue. This causes weakness of the calf muscles leading to difficulty in push-off activities like running or even brisk walking.</p>
                    </div>

                    <div class="content-section" id="treatment">
                        <h3>How is it treated?</h3>
                        <p><strong>Surgical repair:</strong> Young, active, fit individuals are advised to undergo repair of their ruptured tendon. Surgical repair has lower risk of rupture and advantage of earlier return to normal activities, including sports. In surgery the ends of torn tendon are approximated and stitched together using minimally invasive techniques, which cause minimal damage to surrounding tissues, thus bringing better outcomes. A boot or a brace is given till it heals.</p>
                        
                        <p><strong>Non-surgical treatment:</strong> The leg is immobilized in a plaster cast to approximate the torn end as close as possible. This treatment may be prolonged and associated with weakness of calf muscles and an increase risk of re-rupture compared to surgical treatment.</p>
                    </div>

                    <div class="content-section" id="recovery">
                        <h3>When can I return to activities of daily living?</h3>
                        <p>If the tendon is treated promptly in the most appropriate way, most individuals can return to jogging after 12 weeks and jumping sports in 6-9 months. Office work (sitting on a desk) can be resumed within a week of wound healing. If your job involves heavy labour, it may take 3-4 months to be comfortable at work.</p>
                    </div>

