                    <div class="content-section" id="tendon-changes">
                        <h3>What happens to the tendon?</h3>
                        <p>The tendon is made up of strong fibres formed of collagen, which becomes weaker and less flexible as we age. This leads to tiny microscopic tears that can develop, leading to weakness, pain & eventually swelling. Swelling develops in the middle of the tendon because this area has a less well developed blood supply so is unable to heal itself as effectively.</p>
                    </div>

                    <div class="content-section" id="causes">
                        <h3>What causes Achilles tendon pain?</h3>
                        <p>Pain in the Achilles tendon, more common among the middle-aged athletes, can be caused by a number of factors, including age related degeneration, a rapid increase or change in training regime. However, in some cases, tendon can sometimes become painful for no apparent external factors, this may be due to arthritis or other inflammatory disorders.</p>
                    </div>

                    <div class="content-section" id="symptoms">
                        <h3>What are the symptoms?</h3>
                        <ul>
                            <li>Pain along the tendon or back of the heel that is worse in the morning or at the beginning of any activity.</li>
                            <li>Swelling that is there all the time and can become worse throughout the day with activity.</li>
                            <li>Severe pain the day after exercises.</li>
                            <li>Thickening of the tendon.</li>
                        </ul>
                    </div>

                    <div class="content-section" id="tendinosis">
                        <h3>What is Achilles tendinitis (Achilles tendinosis)?</h3>
                        <p>This is a widely used term for anyone with Achilles pain. It is not strictly accurate, however, because pain is usually caused by age related changes (tissue degeneration) leading to derangement in the tendon fibres (tendinosis) rather than inflammation (tendinitis) of the tissue. It's therefore more accurate to use the term 'Achilles tendinosis'. There are two types of tendinosis:</p>
                        <ul>
                            <li><strong>Insertional:</strong> when pain, weakness and loss of function due to healthy Achilles tendon being compressed against a prominent bone or a degenerated tendon which develops bone within the substance of the tendon (bone spurs). This causes inflammation of a small bursa (the fluid filled sac behind the tendon), known as bursitis.</li>
                            <li><strong>Non insertional:</strong> when pain, weakness and loss of function is associated with swelling in the main portion of the tendon and not at the the bottom by the heel bone.</li>
                        </ul>
                    </div>

                    <div class="content-section" id="diagnosis">
                        <h3>How is it diagnosed?</h3>
                        <p>A detailed history of your symptoms along with clinical examination can help you diagnose the problem. Further investigation like a x-ray can show tissue swelling or bone spurs; however, an MRI scan can be more effective as it shows the tendon quality in more detail.</p>
                        <p><strong>Can the problem worsen if delayed treatment?</strong><br>Surprisingly, delaying treatment for Achilles problem is not rare (1 in 4 cases delay). People often put up with the symptoms for some time, or alter their level of activity, before seeking medical advice. However, symptoms can become worse and there can be a rupture of the tendon.</p>
                    </div>

                    <div class="content-section" id="treatment">
                        <h3>How is it treated?</h3>
                        <p><strong>Non-operative treatment:</strong> most patients with tendinosis don't need to have surgery. Reducing your level of activity, resting and taking anti-inflammatory medication along with physiotherapy can really make a difference. You may be advised to use a small silicone insert in your shoe to raise your heel to relieve the irritation of the tendon from the heel bone.</p>
                        <p><strong>Surgery:</strong> if the tendon does not recover and pain persists for more than six months, you may be offered to explore the painful tendon area and remove any damaged or inflamed tissue, or painful bony bumps on the heel. If the tendon is detached from the bone during surgery or a large amount of the tendon is removed, then tendon replacement surgery using tendon in the foot to support the damaged Achilles tendon may be needed.</p>
                    </div>

