                    <div class="content-section" id="causes">
                        <h3>What causes it?</h3>
                        <p>You are more likely to have ankle arthritis if you have</p>
                        <ul>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Had a previous fracture or bad ankle sprain</li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Inflammatory arthropathy – rheumatoid arthritis or gout</li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Infection in your ankle – bacterial or tuberculosis</li>
                        </ul>
                    </div>

                    <div class="content-section" id="symptoms">
                        <h3>What are the symptoms?</h3>
                        <p>Pain is the prime complaint. Pain aggravates on moving or bearing weight on the affected ankle, caused by roughening of joint. Initially the foot or ankle can be quite swollen as the joint lining become inflamed and warmer to feel. Fitting into daily footwear might be difficult and pain can be progressively worse. Severely affected ankle may become deformed, making walking difficult.</p>
                    </div>

                    <div class="content-section" id="diagnosis">
                        <h3>Not all ankle pain is ankle arthritis</h3>
                        <p>There are a large number of other causes of pain from the ankle joint which are separate entities from ankle arthritis. Examples are conditions such as an osteochondral defect, synovitis of the ankle joint, arthrofibrosis of the ankle joint and avascular necrosis of the talus.</p>
                    </div>

                    <div class="content-section" id="identification">
                        <h3>Is it really ankle arthritis?</h3>
                        <p>The ankle joint sits very close in particular to the sub-talar joint. Situations exist where both ankle arthritis and sub-talar arthritis are present or in the sub-talar joint it may be affected without the ankle joint being affected (and vice versa). It can sometimes be difficult by examination and plain x-ray to tell which the relevant joint is. Under these circumstances it can be useful to inject local anaesthetic into the joint under question to see whether this produces temporary relief and therefore identifies the joint directly as being the relevant one that is producing pain.</p>
                    </div>

                    <div class="content-section" id="treatment">
                        <h3>How is it treated?</h3>
                        <p><strong>Non-operative treatment:</strong></p>
                        <ul>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Using a brace or supportive footwear to stabilize your ankle. Cushioned shoes may absorb impact on walking.</li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Analgesics & anti-inflammatory tablets may help reduce acute exacerbation.</li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Local anesthetic with steroid may be injected to provide symptomatic relief.</li>
                        </ul>
                        <p><strong>Surgery:</strong></p>
                        <ul>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;If the pain interferes with your activities of daily living, affects your sleep, despite taking painkillers & anti-inflammatory tablets, you may need an ankle arthroscopy (key hole surgery) to tidy up the joint and restore movement. (Debridement)</li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Other options include ankle fusion surgery, which may stiffen your ankle joint eliminating pain or an ankle replacement surgery.</li>
                        </ul>
                    </div>

                    <div class="content-section" id="surgery-comparison">
                        <h3>Is joint fusion better than ankle replacement?</h3>
                        <p>In an ankle fusion surgery, the ankle joint is permanently stiffened in a position that will allow people to maintain a very active lifestyle, even jogging, running or dancing. Some footwear modification may be needed to walk easier. Joint replacement aims to maintain movement; however, it's less predictable in its long-term outcome, especially in younger active people. As technology & designs improves, the success of ankle replacements will continue to grow.</p>
                    </div>

