                        <h3>What causes midfoot arthritis?</h3>
                        <p>There are different reasons for developing arthritis in the small joints in the midfoot. These include:</p>
                        <ul class="causes-list">
                            <li><i class="fa fa-check-square-o"></i> <strong>Post-traumatic</strong> - Bone fracture or damage/dislocation at any of the bones or joints, even if successfully treated many years before</li>
                            <li><i class="fa fa-check-square-o"></i> <strong>Rheumatoid arthritis</strong> - Patients with rheumatoid or other forms of inflammatory arthritis can also develop arthritis in their foot</li>
                            <li><i class="fa fa-check-square-o"></i> <strong>Osteoarthritis</strong> - Even where there is no injury, arthritis can develop without explanation in the midfoot</li>
                            <li><i class="fa fa-check-square-o"></i> <strong>Tibialis posterior tendon dysfunction</strong> - If this tendon in the foot develops problems then the arch can flatten and you can develop midfoot or hindfoot arthritis</li>
                        </ul>
