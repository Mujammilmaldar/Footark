                    <div class="content-section" id="causes">
                        <h3>What causes a cavus foot?</h3>
                        <p>Cavus foot is often caused by a neurologic disorder or other medical condition such as polio, cerebral palsy, Charcot-Marie-Tooth disease, spina bifida, muscular dystrophy, or stroke. Malunited heel bone or ankle bone fractures and burns or compartment syndrome involving the leg can also cause a high arch foot. In other cases of cavus foot, the high arch may represent an inherited structural abnormality. An accurate diagnosis is important because the underlying cause of cavus foot largely determines its future course. If the high arch is due to a neurologic disorder or other medical condition, it is likely to progressively worsen. On the other hand, cases of cavus foot that do not result from neurologic disorders usually do not change in appearance.</p>
                    </div>

                    <div class="content-section" id="problems">
                        <h3>What problem does a cavus foot cause?</h3>
                        <p>The arch of a cavus foot will appear high even when standing. In addition, one or more of the following symptoms may be present:</p>
                        <ul>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Hammertoes (bent toes) or claw toes (toes clenched like a fist) </li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Hard painful skin (Calluses) on the great toe, side, or heel of the foot </li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Pain when standing or walking </li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;An unstable foot due to the heel tilting inward, which can lead to ankle sprains </li>
                        </ul>
                        <p>Some people with cavus foot may also experience foot drop, a weakness of the muscles in the foot and ankle that results in dragging the foot when taking a step. Foot drop is usually a sign of an underlying neurologic condition.</p>
                    </div>

                    <div class="content-section" id="testing">
                        <h3>I have high arches – should I be having tests done for all the diseases?</h3>
                        <p>If your arches are appearing to get higher or if your complaints are increasing – increasing pain and tendency to twist your ankle, you should consult a specialist orthopedic surgeon. Otherwise it probably depends on how much trouble your feet are giving you.</p>
                    </div>

                    <div class="content-section" id="diagnosis">
                        <h3>How is it diagnosed? Can I do anything myself?</h3>
                        <p>Diagnosis of cavus foot includes a review of the patient's history and detailed clinical examination. The foot and ankle surgeon examines the foot, looking for a high arch and possible calluses, hammertoes, and claw toes. The foot is tested for muscle strength, and the patient's walking pattern and coordination are observed. If a neurologic condition appears to be present, the entire limb may be examined. The surgeon may also study the pattern of wear on the patient's shoes. X-rays are sometimes ordered to further assess the condition. In addition, the surgeon may refer the patient to a neurologist for a complete neurologic evaluation. Non-Surgical TreatmentNon-surgical treatment of cavus foot may include one or more of the following options:</p>
                        <ul>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Orthotic devices. Custom orthotic devices that fit into the shoe can be beneficial because they provide stability and cushioning to the foot. </li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Shoe modifications. High-topped shoes support the ankle, and shoes with heels a little wider on the bottom add stability. </li>
                            <li><i class="fa fa-check-square-o"></i> &nbsp;&nbsp;Bracing. The surgeon may recommend a brace to help keep the foot and ankle stable. Bracing is also useful in managing foot drop. </li>
                        </ul>
                    </div>

                    <div class="content-section" id="surgery">
                        <h3>When is Surgery Needed?</h3>
                        <p>If non-surgical treatment fails to adequately relieve pain and improve stability, surgery may be needed to decrease pain, increase stability, and compensate for weakness in the foot. The surgeon will choose the best surgical procedure or combination of procedures based on the patient's individual case. In some cases where an underlying neurologic problem exists, surgery may be needed again in the future due to the progression of the disorder</p>
