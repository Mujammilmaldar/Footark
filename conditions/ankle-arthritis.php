                        <h2>Understanding Ankle Arthritis</h2>
                        <div class="content-with-image">
                            <div class="content-text">
                                <p>The ankle joint is formed between the shin bone (tibia) and the ankle bone (talus). It allows up and down movements of your foot. It bears 5-7 times the body weight during day-to-day activities like standing & running.</p>
                                
                                <p>Ankle arthritis is a condition where the smooth cartilage lining of the bones inside the joint, which allows normal smooth motion between the bones, become worn and gradually cause friction between the bones when you move.</p>
                            </div>
                            <div class="content-image-card">
