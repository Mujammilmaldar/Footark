                        <h3>What causes flat feet?</h3>
                    </div>

                    <div class="content-section" id="physiological">
                        <h3>Physiological or Normal Flat Feet:</h3>
                        <p>The vast majority of flat feet fall into this group. The commonest cause of a flat foot is the so-called "physiological flat foot." This means that the flatness is a variation of normal. The arch is flattened, but the foot is supple, pain free and functional. When you stand on tiptoes the arch of the foot re-establishes.</p>
                        
                        <p>If you have physiologically flat feet you can be reassured that there is no structural abnormality needing treatment. Shoes tend to wear out more quickly in people with flat feet, but simply wearing comfortable shoes is the best initial treatment. You should try to buy shoes with an arch support and a well-constructed, supportive heel.</p>
                    </div>

                    <div class="content-section" id="pathological">
                        <h3>Pathological or Painful Flat Feet:</h3>
                        <p>A small number of people have flat feet, which are a problem. They may require formal treatment. Such feet are said to be "pathological" – which just means abnormal. The causes differ in children and adults. In children the foot may not develop normally in the womb. The small bones of the foot (tarsals) do not separate properly. The foot is stiff and painful. You may hear your doctors talking about a tarsal coalition – this is just another way of saying that the bones have not separated. You may hear people saying that there is a "bar" – this refers to the abnormal segment of bone, or bar, connecting the bones. Tarsal coalitions, or bars, tend to first be noticed in children or young adults. The commonest cause of flat foot in adults is stretching of the tibialis posterior tendon, this tendon lifts the arch of the foot. This tibialis posterior initiates push off as you walk and supports the arch. In some people, usually those with a physiological flat foot to start with, the tendon can become worn. With wear the tendon does not usually snap, but stretches. If it stretches more than 1cm it loses all of its effectiveness, and support of the arch is lost. The arch of the foot then flattens. Occasionally individuals are born with an extra bone (accessory navicular) in the tibialis posterior tendon. This extra bone is present in about 10% of people. It is usually pain free, and you may just notice a small prominence over the inside of the arch. The accessory navicular does not need treatment unless it is painful, or "symptomatic."</p>
                        
                        <p>A previous fracture of your heel bone can lead to flat feet; the pain from this is due to wear and tear (osteoarthritis) in the joint below the ankle– the subtalar joint. All types of arthritis – including osteo- and rheumatoid arthritis – can cause painful flat feet. Arthritis in the midfoot can lead to a flat foot as well.</p>
                    </div>

                    <div class="content-section" id="symptoms">
                        <h3>What are the symptoms of flat feet?</h3>
                        <p>Often the patient has few symptoms, other than the loss of height of the arch. Nevertheless the stretched tendon on the inside of the ankle can be swollen and painful. As the foot flattens the pain moves to the outside of the ankle, as the tendons are trapped under the fibula and the heel bone. The foot appears turned out. The pain can be associated with stiffness, and it may be difficult to walk on uneven or cobbled surfaces. The foot looses its side-to-side movement. In very advanced cases the pain can also affect the ankle joint, as at this stage the ankle joint starts to wear unevenly and tilts. As the arch collapses rough skin (callosities) can develop under the inner border of the foot due to the foot rolling inwards and the arch collapsing. The Achilles tendon or heel cord also tightens.</p>
                    </div>

                    <div class="content-section" id="treatment">
                        <h3>Treatment of flat feet:</h3>
                        <p>If your flat feet are painless as a result of ligament hyperlaxity surgical treatment is not necessary. If your feet are uncomfortable a supportive insole with an arch support may be helpful. Insoles are also called "orthoses". Customised insoles can be very expensive, and it is often better to start by trying shop bought arch supports. Some shoes (for example training shoes) are sold with an arch support in them; alternatively the arch support can be bought independently in a pharmacy. It should be noted that insoles do not change the shape of your feet in the long-term and it may not be satisfactory for a more active person, who may need corrective surgery to reform the arch.</p>
                    </div>

