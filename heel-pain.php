                    <div class="content-section" id="causes">
                        <h3>What causes plantar fasciitis?</h3>
                        <p>You are at a greater risk for developing plantar fasciitis if you are overweight or obese. This is due to the increased pressure on your plantar fascia ligaments, especially if you have sudden weight gain.</p>
                        
                        <p>If you are a long distance runner, you may be more likely to develop plantar fascia problems. You are also at risk if you have a very active job that involves being on your feet often, such as a factory worker or a restaurant server. Active men and women between the ages of 40 and 70 are at the highest risk for developing plantar fasciitis. It is also slightly more common in women than men.</p>
                        
                        <div class="highlight-box">
                            <h4>Risk Factors</h4>
                            <p>If you have foot problems, such as very high arches / flat feet or a tight Achilles tendon (the tendons attaching the calf muscles to the heels), you may be prone for plantar fascia pain.</p>
                            
                            <p><strong>Important Note:</strong> Heel spurs do not cause plantar fasciitis. A heel spur is a hook of bone that can form on the heel bone (calcaneus) of the foot. One out of every 10 people has a heel spur, but only one out of 20 people with heel spurs experience pain.</p>
                        </div>
                    </div>

                    <div class="content-section" id="symptoms">
                        <h3>What Are the Symptoms of Plantar Fasciitis?</h3>
                        <p>The major complaint of those with plantar fasciitis is pain and stiffness in the bottom of the heel. This develops gradually over time. It usually affects just one foot, but can affect both feet. Some people describe the pain as dull, while others experience a sharp pain, and some feel a burning or ache on the bottom of the foot extending outward from the heel.</p>
                        
                        <p>The pain is usually worse in the morning when you take your first steps out of bed, or if you've been sitting or lying down for a while. Climbing stairs can be very difficult due to the heel stiffness.</p>
                        
                        <p>After prolonged activity, the pain can flare-up due to increased inflammation. Pain is not usually felt during the activity, but rather just after stopping.</p>
                    </div>

                    <div class="content-section" id="diagnosis">
                        <h3>How Is Plantar Fasciitis Diagnosed?</h3>
                        <p>It can be diagnosed during a consultation with your specialist, without the need of preliminary x-ray. Your doctor will perform a physical exam to check for tenderness in your foot and the exact location of the pain to make sure that it's not caused by a different foot problem.</p>
                        
                        <p>If pain persists after initial management, X-rays or a magnetic resonance imaging (MRI) scan may be ordered to check that nothing else is causing your heel pain, such as a bone fracture.</p>
                    </div>

                    <div class="content-section" id="treatment">
                        <h3>What Treatments Are Available for Plantar Fasciitis?</h3>
                        <p>Reducing inflammation in the plantar fascia ligament is an important part of treatment, though this does not address the underlying damage to the ligament.</p>
                        
                        <h4>Home Treatments</h4>
                        <p>Initial home treatment includes staying off your feet and applying ice for 15 to 20 minutes three or four times a day to reduce swelling. You can also try reducing or changing your exercise activities. Using arch supports in your shoes and doing stretching exercises may also help to relieve pain. Nonsteroidal anti-inflammatory drugs (NSAIDS), are often used to reduce inflammation in the ligament.</p>
                        
                        <p>Physical therapy is an important part of treatment for planter fasciitis. It can help stretch your plantar fascia and Achilles tendons. A physical therapist can also show you exercises to strengthen your lower leg muscles, helping to stabilize your walk and lessen the workload on your plantar fascia.</p>
                    </div>

                    <div class="content-section" id="advanced-treatment">
                        <h3>Advanced Treatment Options</h3>
                        <p>If pain continues and other methods aren't working, your doctor may recommend extracorporeal shock wave therapy. Sound waves are bombarded against your heel to stimulate healing within the ligament. This treatment can result in bruises, swelling, pain, and numbness, and has not been proven to be consistently effective in relieving symptoms.</p>
                        
                        <p>The most dramatic therapy, used only in cases where pain is very severe, is surgery. The plantar fascia can be partially detached from the heel bone, but the arch of the foot is weakened. Another surgery involves lengthening the calf muscle, a process called gastrocnemius recession.</p>
                    </div>

                    <div class="content-section" id="braces">
                        <h3>Braces and Supports</h3>
                        <p>Night splints are another treatment that can help stretch your calf and the arch of your foot. Night splints are a type of brace that holds your foot in a flexed position and lengthens the plantar fascia and Achilles tendon overnight. This can prevent morning pain and stiffness.</p>
                    </div>

                    <div class="content-section" id="prognosis">
                        <h3>What Can I Expect in the Long-Term?</h3>
                        <p>Most people do not need surgery to relieve pain. However, treatment can last from several months to two years before symptoms improve.</p>
                        
                        <div class="highlight-box">
                            <h4>Potential Complications</h4>
                            <p>If you ignore the condition, you can develop chronic heel pain. This can change the way you walk and cause injury to your legs, knees, hips and back. Steroid injections and some other treatments can weaken the plantar fascia ligament and cause potential rupture of the ligament.</p>
                        </div>
