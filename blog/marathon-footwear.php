            <!-- Support/Stability Section -->
            <article class="blog-content-card" id="support-stability">
                <div class="blog-card-header">
                    <h2>Support / Stability</h2>
                </div>
                <div class="blog-card-body">
                    <p>Many of us do not have perfect shape, arch or gait as defined by modern foot experts like us. However, you should note that shape/arch or the gait in whichever form it is, is mostly not a problem in itself (there goes the definition of perfect feet out of window! So much to call ourselves an expert!!). What's the problem if there is no problem. Let's understand it.</p>

                    <p>When we run foot and ankle play a crucial role, as they absorb the impact of landing and generate the force for propulsion. The heel and the ankle are responsible for stabilizing the leg during initial contact with the ground. The arch and the five metatarsal bones transfer the weight from the rearfoot to the forefoot. The toes and the ball of the foot are responsible for providing leverage and thrust during toe-off.</p>
                    
                    <p>The type of foot affects the pronation or supination of the foot during running, which is the inward or outward rolling of the foot respectively. Pronation and supination are normal motions that help absorb shock and adapt to uneven surfaces. In some cases, excessive or insufficient pronation or supination can lead to injuries or inefficiencies in running.</p>
                    
                    <p>Stability/ motion control footwear is made with extra support at required positions to help control or in extreme cases correct the running gait of the runner. While stability footwear is available everywhere, motion control footwear is found only in specialised shops. Though one should neither use stability or motion control shoes unless the condition is identified by an expert, or it may cause damage. Do not go solely by recommendations of sales executives at stores - most of us do not need such support in general.</p>

                    <div class="blog-medical-note">
                        <h4>Pro Tip</h4>
                        <p>While one should get an expert opinion to evaluate pronation/supination, one at home check could be to check your well-worn footwear and observe the outsole. If any one area/side is more rubbed-off, it may indicate that you do not have a neutral gait - don't conclude it's a problem - take opinion from expert doctors.</p>
                    </div>
                </div>
            </article>

