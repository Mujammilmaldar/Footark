                <div class="blog-card-header">
                    <h2>Nutrition</h2>
                </div>
                <div class="blog-card-body">
                    <p>Nowadays there are distinct options in terms of energy meals, nutrient bars, and energy drinks being advertised for their utility during marathon training. It may be true (mostly not!).</p>

                    <p>While we describe what nutrients are required for that marathon finish, we do not suggest you try any new meal plan or dish. Try to get the nutrients from the food your body is accustomed to.</p>

                    <h3>Carbohydrates - Your Main Fuel</h3>
                    <p>While a balanced diet that provides all nutrients is recommended, Carbohydrates, "the immediate energy store of the human body," is the main fuel for your muscles. Carbohydrate is the main source of fuel for marathon runners, but your body can only store a limited amount of it. When you run out of carbohydrates, your energy levels drop, and your pace slows down. You may even experience the 'wall' - a state where your body has to rely on fat as fuel, which is much harder to convert to energy.</p>

                    <p>To avoid this, you should increase your carbohydrate intake in the days before your race and consume carbohydrates during the marathon. This will help you maintain your energy and performance throughout the race.</p>

                    <p>Increase your intake of foods like bread, pasta, rice, potatoes, fruits, and vegetables in the last three days before the marathon. <strong>DO NOT OVEREAT</strong> - just the composition of the meal be more carb-based.</p>

                    <p>Avoid any foods that could upset your stomach or cause bloating, gas, or diarrhoea, such as spicy, greasy, or high-fibre foods. Drink plenty of water and electrolyte drinks to stay hydrated and avoid alcohol and caffeine.</p>

