                    <h2>Physical Training</h2>
                </div>
                <div class="blog-card-body">
                    <p>There is a reason the week before the marathon is called 'Taper Week.' The last week of training is not the time to push yourself hard or try new workouts. You want to maintain your fitness level, but also allow your body to recover and rest.</p>

                    <p>- The general rule is to reduce your mileage by 50% in the last week and do one or two easy runs of 5 to 8 km. Aim for some easy strides or speed bursts to keep your legs fresh.</p>

                    <p>- You can also do some light cross-training, such as swimming, cycling, or yoga, but avoid anything that could cause soreness or injury.</p>

                    <p>- The day before the marathon, you can do a light jog or walk for 10 to 15 minutes or take a complete rest day.</p>

