                    <h2>Training Injury Prevention</h2>
                </div>
                <div class="blog-card-body">
                    <p>One of the most important aspects of marathon training is injury prevention.</p>

                    <p>Running a marathon puts a lot of stress on your body, especially on your feet, ankles, knees, hips, and lower back. Some of the most common injuries that marathon runners face are blisters, chafing, shin splints, runner's knee, iliotibial band syndrome (ITBS), plantar fasciitis, Achilles tendinitis, stress fractures, muscle cramps, dehydration, heat stroke, hypothermia, and hyponatremia (low sodium levels).</p>

                    <div class="blog-medical-note">
                        <h4>Don't Be Discouraged</h4>
                        <p>If you are discouraged by the list, you still need to mentally prepare for the activity. These are injuries that are avoidable/treatable and are not big enough to stop you from running.</p>
                    </div>

                    <h3>Basic Guidelines for Injury Prevention:</h3>

                    <p>• Wear proper running shoes that fit well and provide adequate cushioning and support. Replace them when they show signs of wear. Read this article on <a href="marathon-footwear.php" style="color:#247ad7;">how to choose footwear for a marathon.</a></p>

                    <p>• Wear comfortable running clothes that are breathable and moisture-wicking.</p>

                    <p>• Warm up before each run with some dynamic stretches and light jogging.</p>

                    <p>• Cool down after each run with some static stretches and gentle walking.</p>

                    <p>• Follow the 10% rule: do not increase your weekly mileage or intensity by more than 10% per week.</p>

                    <p>• Vary your running surfaces and routes to avoid repetitive stress on the same muscles and joints.</p>

                    <p>• Hydrate well before, during, and after each run. Drink water according to your thirst and sweat rate.</p>

                    <p>• Eat a balanced diet that provides enough carbohydrates, protein, fat, vitamins, minerals, and antioxidants.</p>

                    <ul>
                        <li>- Carbohydrates are your main source of energy for running, so you should consume about 60% to 70% of your daily calories from carbs.</li>
                        <li>- Protein helps with muscle repair and recovery, so you should consume about 15% to 20% of your daily calories from protein.</li>
                        <li>- Fat provides essential fatty acids and helps with hormone production and absorption of fat-soluble vitamins, so you should consume about 20% to 25% of your daily calories from fat.</li>
                        <li>- Vitamins, minerals, and antioxidants help with immune function and prevent oxidative stress, so you should eat a variety of fruits, vegetables, nuts, seeds, and whole grains.</li>
                        <li>- Listen to your body and take a rest day when needed. Do not run through pain or discomfort. Seek medical attention if you have any signs of injury or illness.</li>
                        <li>- Get enough sleep and rest. Sleep is vital for muscle recovery and mental health. Aim for at least seven to nine hours of quality sleep per night. Try to maintain a regular sleep schedule and avoid napping during the day.</li>
                    </ul>

